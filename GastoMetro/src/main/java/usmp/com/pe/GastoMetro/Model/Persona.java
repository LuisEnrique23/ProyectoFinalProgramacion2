package usmp.com.pe.GastoMetro.Model;

public class Persona {
	int IdPersona;
	String nomPer;
	String apePer;
	String fecNac;
	String docIdent;
	String IdRegCred;
	String IdPais;
	String tipoReg;
	String estado;
	public int getIdPersona() {
		return IdPersona;
	}
	public void setIdPersona(int idPersona) {
		IdPersona = idPersona;
	}
	public String getNomPer() {
		return nomPer;
	}
	public void setNomPer(String nomPer) {
		this.nomPer = nomPer;
	}
	public String getApePer() {
		return apePer;
	}
	public void setApePer(String apePer) {
		this.apePer = apePer;
	}
	public String getFecNac() {
		return fecNac;
	}
	public void setFecNac(String fecNac) {
		this.fecNac = fecNac;
	}
	public String getDocIdent() {
		return docIdent;
	}
	public void setDocIdent(String docIdent) {
		this.docIdent = docIdent;
	}
	public String getIdRegCred() {
		return IdRegCred;
	}
	public void setIdRegCred(String idRegCred) {
		IdRegCred = idRegCred;
	}
	public String getIdPais() {
		return IdPais;
	}
	public void setIdPais(String idPais) {
		IdPais = idPais;
	}
	public String getTipoReg() {
		return tipoReg;
	}
	public void setTipoReg(String tipoReg) {
		this.tipoReg = tipoReg;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}	
}
