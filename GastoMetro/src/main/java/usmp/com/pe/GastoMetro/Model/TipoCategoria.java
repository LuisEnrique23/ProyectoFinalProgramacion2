package usmp.com.pe.GastoMetro.Model;

public class TipoCategoria {
	String IdTipCat;
	String nomTip;
	String descTip;
	String abrevTip;
	public String getIdTipCat() {
		return IdTipCat;
	}
	public void setIdTipCat(String idTipCat) {
		IdTipCat = idTipCat;
	}
	public String getNomTip() {
		return nomTip;
	}
	public void setNomTip(String nomTip) {
		this.nomTip = nomTip;
	}
	public String getDescTip() {
		return descTip;
	}
	public void setDescTip(String descTip) {
		this.descTip = descTip;
	}
	public String getAbrevTip() {
		return abrevTip;
	}
	public void setAbrevTip(String abrevTip) {
		this.abrevTip = abrevTip;
	}
	
}
